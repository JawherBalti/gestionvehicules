Bonjour
cette application mobile est developpe par php en backend et react native
en frontend. l'utilisateur peut creer un compte et se connecter
le premier utilisateur crée aura le role admin et les utilisateur qui sont
créés apres auront le role chauffeur
l'admin peut ajouter des vehicules en indiquant la marque, le modele,
l'immatriculation et une image
il peut aussi consulter la liste des vehicules disponibles et voir l'historique
des achats, suivis et nettoyages
l'admin ou le chauffeur peuvent ajouter un suivi quotidien en ajoutant le
kilometrage, le carburant et la preuve sous forme d'une image ou video
ils peuvent aussi ajoutent des images ou videos de l'etat de la vehicule
avant et apres nettoyage. pour cet exemple je vais ajouter une très courte 
video pour pouvoir la telecharger sur cloudinary gratuitement
j'ajoute une video avant nettoyage et 2 images apres nettoyage
l'achat du carburant se fait de manière similaire au suivi quotidien
il suffit d'ajouter le nombre de litres, le montant et la facture
maintenant l'admi peut consulter la liste des vehicules et voir l'historique
des activités
le compte chauffeur peut faire tous sauf ajouter les vehicules et voir 
l'historique 